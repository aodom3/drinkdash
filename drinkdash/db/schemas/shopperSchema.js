const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const shopperSchema = new Schema({
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true
    },
    city: {
        type: String,
        required: true
    },
    state: {
        type: String,
        required: true
    },
    zip: {
        type: Number,
        required: true
    },
});



module.export = mongoose.model('Shopper', shopperSchema);